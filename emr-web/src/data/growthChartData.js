export function getGrowthData(type, gender) {
  const g = gender === 'F' ? 'GIRLS' : 'BOYS';
  if (type === 'weight') return gender === 'F' ? WEIGHT_GIRLS : WEIGHT_BOYS;
  if (type === 'height') return gender === 'F' ? HEIGHT_GIRLS : HEIGHT_BOYS;
  if (type === 'bmi')    return gender === 'F' ? BMI_GIRLS    : BMI_BOYS;
  return [];
}

export function calcBMI(weight, height) {
  if (!weight || !height) return null;
  const h = parseFloat(height) / 100;
  const w = parseFloat(weight);
  if (!h || !w) return null;
  return (w / (h * h)).toFixed(1);
}
