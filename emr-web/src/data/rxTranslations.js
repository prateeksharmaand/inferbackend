// ── Smart translators ─────────────────────────────────────────────────────────
export function translateTiming(str, lang) {
  if (!str || lang === 'en') return str;
  const t = getLang(lang);
  const lower = str.toLowerCase().trim();
  return t.timing[lower] || str;
}

export function translateFrequency(str, lang) {
  if (!str || lang === 'en') return str;
  const t = getLang(lang);
  const upper = str.trim().toUpperCase();
  if (t.freq[upper]) return t.freq[upper];
  const exact = t.freq[str.trim()];
  if (exact) return exact;
  return str;
}

export function translateDuration(str, lang) {
  if (!str || lang === 'en') return str;
  const t = getLang(lang);
  return str.replace(/\b(day|days|week|weeks|month|months)\b/gi, (m) =>
    t.dur[m.toLowerCase()] || m
  );
}

export function translateDose(str, lang) {
  if (!str || lang === 'en') return str;
  const t = getLang(lang);
  return str.replace(/\b(tablet|tablets|capsule|capsules|drop|drops|ml|mg|sachet|teaspoon|puff|unit|units)\b/gi, (m) =>
    t.dose[m.toLowerCase()] || m
  );
}
